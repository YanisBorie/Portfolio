function openPopup()
{
	document.getElementById('overlay').style.animation='1.25s fondu both';
	document.getElementById('overlay').style.display='block';
	document.getElementById('popup').style.animation='1.25s fondu both';
	document.getElementById('popup').style.display='block';	
}
function closePopup()
{
	document.getElementById('overlay').style.animation='3s unfondu both';
	document.getElementById('overlay').style.display='none';
	document.getElementById('popup').style.animation='3s unfondu both';
	document.getElementById('popup').style.display='none';	
}
